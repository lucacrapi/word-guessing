// One-off generator: merges the curated German noun/adjective vocabulary
// (already in words-de.mjs) with the curated adjective list and the
// dictionary-derived verb list, dedupes, sorts, and rewrites words-de.mjs.
import { writeFileSync } from "fs";
import { WORDS as EXISTING } from "./words-de.mjs";
import { ADJECTIVES } from "./adjectives-de.mjs";
import { VERBS } from "./verbs-de.mjs";

const combined = [...new Set([...EXISTING, ...ADJECTIVES, ...VERBS])].sort();

const body = combined.map((w) => JSON.stringify(w)).join(",");
const content = `// Curated vocabulary of common German nouns, adjectives, and verbs used as the
// game dictionary (lowercase). See adjectives-de.mjs (adjectives) and
// verbs-de.mjs (verbs) for additional source lists; this file is generated by
// scripts/build-words-de.mjs.
export const WORDS = [
${body}
];
`;

writeFileSync(new URL("./words-de.mjs", import.meta.url), content);
console.log(`Existing: ${EXISTING.length}, adjectives: ${ADJECTIVES.length}, verbs: ${VERBS.length}, combined unique: ${combined.length}`);
